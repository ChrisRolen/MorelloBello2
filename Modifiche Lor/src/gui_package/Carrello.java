    private void EliminaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EliminaMouseClicked
        int index = 0;
        
        try {
            // TODO add your handling code here:
            index = Libri.carrello.indexOf(new Libri( (String) ISBN.getSelectedItem()));
        } catch (SQLException ex) {
            Logger.getLogger(Carrello.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Libri.carrello.remove(index);
        Libri.numerodicopie.remove(index);
        SwingUtilities.updateComponentTreeUI(this);
    }//GEN-LAST:event_EliminaMouseClicked


    public Carrello() {
        initComponents();
         DefaultListModel model = new DefaultListModel();
         model.ensureCapacity(Libri.carrello.size());
         
         int i = 0;
         for(Libri a:Libri.carrello){
             model.addElement("Titolo: " + a.getTitle() + " Autore/i: " + a.getAuthor().toString() + " Prezzo: " + a.getPrice() + " Punti: " + a.getNumberofpoints()+ " ISBN: " +
                     a.getISBN()+ "Numero Copie: " + Libri.numerodicopie.get(i));
             i++;
             
         }
         
         Lista.setModel(model);
         
         
         
         int costototale = 0;
         int puntitotali = 0;
         
        for(Libri a:Libri.carrello){
			costototale += a.getPrice();
			puntitotali += a.getNumberofpoints();
		}
        
         Prezzototale.setText(Integer.toString(costototale));
         PuntiTotali.setText(Integer.toString(puntitotali));
         
         DefaultComboBoxModel mode = new DefaultComboBoxModel();
         
         for(Libri a:Libri.carrello){
             mode.addElement(a.getISBN());
             i++;
         }
         ISBN.setModel(mode);
         
         jTextArea1.setEditable(false);
         
         
        
    }

